#include <stdlib.h>

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned char	*cs1;
	unsigned char	*cs2;

	cs1 = (unsigned char *)s1;
	cs2 = (unsigned char *)s2;
	if (n < 1)
		return (0);
	while ((*(cs1++) == *(cs2++)) && n-- > 1)
		;
	return (*(cs1 - 1) - *(cs2 - 1));
}

#include <stdlib.h>
#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*mem;

	if (!(mem = malloc(size * count)))
		return (NULL);
	ft_bzero(mem, count * size);
	return (mem);
}
